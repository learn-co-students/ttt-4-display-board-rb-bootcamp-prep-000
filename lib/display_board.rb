# Define display_board that accepts a board and prints
# out the current state.
def display_board(letter)
puts " #{letter[0]} | #{letter[1]} | #{letter[2]} "
puts "-----------"
puts " #{letter[3]} | #{letter[4]} | #{letter[5]} "
puts "-----------"
puts " #{letter[6]} | #{letter[7]} | #{letter[8]} "
end
