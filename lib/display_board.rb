# Define display_board that accepts a board and prints
# out the current state.


def display_board(currentBoard=[" "," "," "," "," "," "," "," "," "])
puts " #{currentBoard[0]} | #{currentBoard[1]} | #{currentBoard[2]} "
puts "-----------"
puts " #{currentBoard[3]} | #{currentBoard[4]} | #{currentBoard[5]} "
puts "-----------"
puts " #{currentBoard[6]} | #{currentBoard[7]} | #{currentBoard[8]} "
end